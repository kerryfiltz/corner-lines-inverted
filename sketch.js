function setup() {
  createCanvas(400, 400);
}

function draw() {
  background('BLACK');
  
  for(var a = 0; a <= 400; a = a + 10) {
    stroke(220);
    
    line(0, a, 400 - a, 0);
    line(400, 400 - a, a, 400);
    line(400, a, a, 0);
    line(400 - a, 400, 0, 400 - a);
  }
  
  for(var b = 0; b <= 200; b = b + 10) {
    stroke(180);
    
    line(100, 100 + b, 300 - b, 100);
    line(300, 300 - b, 100 + b, 300);
    line(300, 100 + b, 100 + b, 100);
    line(300 - b, 300, 100, 300 - b);
  }
  
  for(var i = 0; i <= 100; i = i + 10) {
    stroke(140);
    
    line(150, 150 + i, 250 - i, 150);
    line(250, 250 - i, 150 + i, 250);
    line(250, 150 + i, 150 + i, 150);
    line(250 - i, 250, 150, 250 - i);
  }
  
  for(var x = 0; x <= 50; x = x + 5) {
    stroke(100);
    
    line(175, 175 + x, 225 - x, 175);
    line(225, 225 - x, 175 + x, 225);
    line(225, 175 + x, 175 + x, 175);
    line(225 - x, 225, 175, 225 - x);
  }
  
  for(var c = 0; c <= 25; c = c + 5) {
    stroke(60);
    
    line(187.5, 187.5 + c, 212.5 - c, 187.5);
    line(212.5, 212.5 - c, 187.5 + c, 212.5);
    line(212.5, 187.5 + c, 187.5 + c, 187.5);
    line(212.5 - c, 212.5, 187.5, 212.5 - c);
  }
  
  for(var d = 0; d <= 12.5; d = d + 5) {
    stroke(20);
    
    line(193.75, 193.75 + d, 206.25 - d, 193.75);
    line(206.25, 206.25 - d, 193.75 + d, 206.25);
    line(206.25, 193.75 + d, 193.75 + d, 193.75);
    line(206.25 - d, 206.25, 193.75, 206.25 - d);
  }
  
  
}
